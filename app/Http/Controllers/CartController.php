    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function addToCart($param)
    {
        $data = explode(",", $param);
        $productId = $data[1];
        $count = $data[0];

        if (Auth::guard('user')->check()) {
            Cart::create([
                'user_id' => Auth::guard('user')->id(),
                'product_id' => $productId,
                'quantity' => $count,
            ]);
        }

    }
